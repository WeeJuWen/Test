// Language management
let currentLanguage = localStorage.getItem('language') || 'zh';

// Initialize language and lang attribute on page load
document.addEventListener('DOMContentLoaded', () => {
    document.documentElement.lang = currentLanguage === 'zh' ? 'zh-CN' : 'en';
});

// Set language and update page
function setLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    
    // Update HTML lang attribute to prevent browser translation prompt
    document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';
    
    updatePageLanguage();
}

// Toggle between English and Chinese
function toggleLanguage() {
    const newLang = currentLanguage === 'en' ? 'zh' : 'en';
    setLanguage(newLang);
}

// Get current language
function getCurrentLanguage() {
    return currentLanguage;
}

// Get translation for a key
function getTranslation(key) {
    const keys = key.split('.');
    let value = translations[currentLanguage];
    for (const k of keys) {
        value = value[k];
    }
    return value;
}

// Helper function to update text content for a given selector
function updateText(selector, text) {
    document.querySelectorAll(selector).forEach(el => el.textContent = text);
}

